import math

# Количество испытаний (бросков монеты)
n = 144

# Количество успехов (орел выпадет ровно 70 раз)
k = 70

# Вероятность успеха в одном испытании (вероятность выпадения орла)
p = 0.5

# Вероятность того, что орел выпадет ровно 70 раз
probability = math.comb(n, k) * (p ** k) * ((1 - p) ** (n - k))

print("Вероятность того, что орел выпадет ровно 70 раз:", probability)
