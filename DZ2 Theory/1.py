import math

# Количество испытаний
n = 100

# Количество успехов (стрелок попадет в цель ровно 85 раз)
k = 85

# Вероятность успеха в одном испытании
p = 0.8

# Вероятность того, что стрелок попадет в цель ровно 85 раз
probability = math.comb(n, k) * (p ** k) * ((1 - p) ** (n - k))

print("Вероятность того, что стрелок попадет в цель ровно 85 раз:", probability)
